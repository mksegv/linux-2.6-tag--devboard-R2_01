#define EXT1_BASE         0x90000000    // 256 Mb
#define EXT2_BASE         0xa0000000    // 256 Mb
#define EXT3_BASE         0xb0000000    // 256 Mb
#define EXT4_BASE         0xc0000000    // 256 Mb
#define EXT5_BASE         0xd0000000    // 256 Mb

